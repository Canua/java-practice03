package prob6;

public class Calc {
	public static int add(int a, int b) {
		int result = a + b;
		return result;
	}

	public static int sub(int a, int b) {
		int result = a - b;
		return result;
	}

	public static int mul(int a, int b) {
		int result = a * b;
		return result;
	}

	public static int div(int a, int b) {
		int result = a / b;
		return result;
	}
}
